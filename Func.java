public class Func {

    public double toplama(int a, int b) {

        return a + b;
    }

    public double cikartma(int a, int b) {
        return a - b;
    }

    public double carpma(int a, int b) {
        return a * b;
    }

    public double bolme(int a, int b) {
        return a / b;
    }


}
